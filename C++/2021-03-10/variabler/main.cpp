#include <iostream>
using namespace std;
// Om variabler
int main()
{
    // int från "integer", heltal på engelska
    // int talar om vad för slags data variablen kan innehålla
    // tal är namnet på variabeln
    // Använd alltid beskrivande namn
    int alder;
    // alder namnet på variabeln
    // = tilldelningsoperatorn
    // 5 data som tilldelas tal
    // "Det som är till vänster om tilldelningsoperatorn tilldelas (får)
    // värdet av det som är till höger"
    alder = 5;
    cout << alder << endl;
    // värdet på en variabel kan variera
    alder = 3;
    cout << alder << endl;


    return 0;
}
